&#8211; Products còn trong thời hạn warranty (từ 2-5 năm, tuỳ sản phẩm).<br />
&#8211; Products được sử dụng đúng mục đích, công năng, theo cách thức phù hợp và hướng dẫn của nhà sản xuất.<br />
