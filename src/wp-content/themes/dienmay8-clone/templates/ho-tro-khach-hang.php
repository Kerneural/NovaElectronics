<h1 class="chinh_sach_title">Policy warranty</h1>

<p>1. Activate warranty<br />
&#8211; An Toan Kitchen will activate the warranty for you immediately upon delivery and installation (warranty code is your phone number).<br />
&#8211; Products purchased from Bep An Toan will have a nationwide warranty.</p>
<p>2. Warranty conditions<br />
2.1 Free warranty<br />
&#8211; Products are still within the warranty period (from 2-5 years, depending on the product).<br />
&#8211; Products are used for their intended purpose and function, in the appropriate manner and according to the manufacturer's instructions.<br />
&#8211; Products have defects or defective components due to the manufacturer.<br />
&#8211; Serial number and warranty seal are intact and not torn.<br />
&#8211; Products provided by Toan Kitchen.<br />
&#8211; Products are not part of the paid warranty group.</p>
<p>2.2 Paid warranty<br />
&#8211; Products have expired warranty period (from 2-5 years, depending on the product).<br />
&#8211; Products are not used for their intended purpose, function, in the appropriate manner and according to the manufacturer's instructions.<br />
&#8211; Products are defective due to improper installation, use, technical requirements, structural changes, or product safety regulations.<br />
&#8211; Products rust and naturally wear out components over time.<br />
&#8211; Products repaired in advance in places not covered by Bep An Toan's warranty system.<br />
&#8211; Products are damaged due to external factors and poor storage.</p>
<p>3. Warranty period<br />
Depending on the item, the warranty period will be from 2 &#8211; 5 years.</p>
<p>4. Warranty category<br />
&#8211; An Toan Kitchen will repair or replace components for new or replacement products based on customers' requirements.<br />
&#8211; Depending on the actual situation, Bep An Toan will consider providing customers with similar products and components for temporary use or other forms of solution acceptable to customers during the warranty period.</p>
