<p>1. Scope of application<br />
Shipping policy applies to orders purchased via hotline, website or Fanpage of Bep An Toan.<br />
&#8211; For customers in the Hanoi area: Company staff will deliver to your home and install for free for you.<br />
&#8211; For more remote areas, we will schedule an appointment and send goods as well as install for you. You will be served within 24 to 48 hours.</p>
<p>2. Shipping costs<br />
&#8211; Free Shipping nationwide: for products from 2,000,000 VND or more.<br />
