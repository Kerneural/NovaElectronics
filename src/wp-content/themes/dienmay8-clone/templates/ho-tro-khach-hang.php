&#8211; Số seri, tem niêm phong tem warranty còn nguyên, không bị rách.<br />
&#8211; Products được cung cấp bởi Bếp An Toàn.<br />
&#8211; Products không thuộc nhóm warranty tính phí.</p>
<p>2.2 Bảo hành tính phí<br />
&#8211; Products hết thời hạn warranty (từ 2-5 năm, tuỳ sản phẩm).<br />
&#8211; Products không được sử dụng đúng mục đích, công năng, theo cách thức phù hợp và hướng dẫn của nhà sản xuất.<br />
&#8211; Products bị lỗi do quá trình lắp đặt, sử dụng không đúng yêu cầu kỹ thuật, thay đổi kết cấu, quy định an toàn của sản phẩm.<br />
&#8211; Products bị rỉ sét, hao mòn tự nhiên của các linh kiện theo thời gian sử dụng.<br />
&#8211; Products sửa chữa trước ở những nơi không nằm trong hệ thống warranty của Bếp An Toàn.<br />
&#8211; Products hư hỏng do yếu tố bên ngoài và do bảo quản không tốt gây ra.</p>
<p>3. Thời hạn warranty<br />
Phụ thuộc vào mặt hàng, thời hạn warranty sẽ từ 2 &#8211; 5 năm.</p>
<p>4. Hạng mục warranty<br />
&#8211; Bếp An Toàn sẽ thực hiện việc sửa chữa hoặc thay thế linh kiện cho sản phẩm mới hoặc sản phẩm thay thế dựa trên yêu cầu của Quý khách hàng.<br />
